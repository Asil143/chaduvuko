export function getSubject(category: string, second: string, slug: string): Subject | undefined {
  return ALL_SUBJECTS.find(s => {
    if (s.category !== category) return false
    if (category === 'school' || category === 'intermediate') return s.classSlug === second && s.slug === slug
    if (category === 'competitive') return s.exam === second && s.slug === slug
    if (category === 'btech') return s.branch === second && s.slug === slug
    return false
  })
}

export function getSubjectsByCategory(category: string): Subject[] {
  return ALL_SUBJECTS.filter(s => s.category === category)
}